package Basics;
import java.util.*;

public class Intro {

	public static void main(String[] args) {
		
		// primitive data types
		int rollno=64;
		
		float marks=98.5f;
		double largeNumbers=1444444.554444;
		long largeInteger=185758489484l;
		
		boolean check =false;
		
		char letter='r';
		
		
		// it is not a primitive data type
		
		String name="Kunal";
		
		System.out.println(name);
		System.out.println(rollno);
		System.out.println(marks);
		System.out.println(largeNumbers);
		System.out.println(largeInteger);
		System.out.println(check);
		System.out.println(letter);
		
	}

}
